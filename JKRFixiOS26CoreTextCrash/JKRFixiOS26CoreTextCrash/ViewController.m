        NSString *safeName = JKRSanitizePlainString(showName, &st);
        if (jkr_isSafe(st) == NO && self.isSafeCheckSwitch.isOn) {
            self.showLabel.text = safeName;
        } else {
            self.showLabel.text = showName;
        }
    }
}

